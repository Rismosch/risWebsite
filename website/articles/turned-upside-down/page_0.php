<p>A lot of things have happened in the last months. I have started
studying again. I got fired. Man what a ride.</p>
<p>But I should start with the most important thing first: The Wii idea
is canceled. It's quite funny actually. Quite literally the <a
href="https://www.rismosch.com/article?id=the-empty-mind-between-milestones" target="_blank" rel="noopener noreferrer">post
before</a> I talked about how fractal-like the progress seems. From the
outside, you'd think you write the engine, you write the game, and
you're done. But actually each step consists of 10 steps, each of which
also take 10 steps, which also also take 10 steps, and so on. Far enough
down you begin to realize you haven't moved at all.</p>

<img src="https://www.rismosch.com/articles/turned-upside-down/sisyphus.webp" alt="sysiphus" style="display: block; margin: auto; max-width: 100%;"/>

<p>I fell into the exact trap I was describing. I have been ignoring all
the progress I've made. Don't get me wrong, the Wii is hella cool, and I
think I <em>have</em> the ability to work on it. But in reality I think
I was stressed out. I guess I couldn't think clearly, probably because I
got fired.</p>
<p>So yeah, uhm, this happened. It was very stressful and ugly. In my
opinion, I've been treated unfairly. But now that the dust is settled, I
am very glad to be gone. I have been decompressing, and looking at it
from the outside made me realize how bad the situation really was. I had
to suppress my identity, trying to fit in, into a circlejerking culture
on top of that. The last 6 years feel like a blur to me. I have only a
handful of positive memories, but my total experience is a net
negative.</p>
<p>The mistake I have made, which ultimately led to my firing, was that
I talked honestly and bluntly. In an effort to improve the situation, I
openly shared my unhappiness. I suggested reducing my hours, so I can
work more on my engine. I genuinely thought a future at the company was
the way forward. But man, their reaction was completely blown out of the
water…</p>
<p>They gave me a notice for Arbeitszeitbetrug (time-sheet fraud), which
is ridiculous, no matter how you look at it. They also gave concrete
working hours (previously I had flexible hours), which completely fucked
over my private life. In particular it made it impossible for me to
attend boardgaming night, which was very important to me, and which
<strong>everyone</strong> at the company knew I've been attending for
the past 3 years. Fucking assholes. Leadership complained how actually,
they have the shit job, and that I don't appreciate the sacrifices they
have made for the company. I do not deny their pain, offered my help
even, but I feel like <em>my</em> pain got overlooked in the process.
The sacrifices I have made? Doesn't matter. They told me I was demanding
too much. And my suffering is not important, and may not even exist.</p>
<p>The worst part in all of this was the AI. Of course I used it. Who
wouldn't? I was never in a situation like this, and I felt really really
lost. I have never used LLM chatbots this extensively before or since
then. Though, I have very clearly hit the limits of AI. It even made me
realize something quite horrific: The LLM cannot give you actual advice.
Period. It can only give information, pulled from it's vast (illegally
gathered) training data, which you yourself then have to filter and
apply. And if you lack the ability to do so, the LLM is going to cause
net harm.</p>

<img src="https://www.rismosch.com/articles/turned-upside-down/Blossom_Light.webp" alt="ChatGPT" style="display: block; margin: auto; max-width: 100%;"/>

<p>The way I used ChatGPT wasn't to just tell it the situation and
asking it what to do next. In multiple chats I tried to lay out the
facts. I tried to be as objective as possible, trying different
approaches. On top of that, I also roleplayed as other people. I
pretended to be my chef, who is upset about this programmer who thinks
he is better than the company. I also pretended to be my teamlead, who
is unsure how to handle the situation where the chef and the lead
programmer got into an argument.</p>
<p>How did ChatGPT reply? Well, no matter what role I played, and no
matter how benevolent or evil I portraid the other side, ChatGPT always
sided with the role prompting it. When I was myself, ChatGPT said
obviously I was correct and I was being treated unfairly. When I
pretended to be my chef, it said the programmer is toxic and is actively
trying to tear down the company. When I pretended to be my teamlead,
ChatGPT said this is not your problem, protect yourself, and fuck them
for fighting.</p>
<p>I got lost in a very bad spiral. You see, Leadership has been pushing
AI more and more. And they directly admitted to me, that they use
chatbots for counseling. And given my experience talking to ChatGPT (as
mentioned above), I realized that leadership genuinely took advice from
a machine, which didn't even try to deescalate the situation. I
realized, that I was trying to navigate a situation, which wasn't
navigatable. There was nothing I could've said or done to improve their
opinion of me. Leadership was mad at me, and the AI reassured them of
these beliefs.</p>
<p>This realization took a heavy toll on my mental health. I spiraled so
badly, I've been trying to come up with some sort of engineered message,
that when my chef were to post it to his AI, it would cause him to
finally see my side. I really obsessed over this. But when I roleplayed
as my chef, prompting "Look what the programmer has said now:
&lt;engineered message&gt;", ChatGPT always doubled down: "The
programmer is clearly gaslighting you, emotional blackmail. He is a
liar, toxic, dangerous, don't trust him. Punish him further." My mental
state became very unstable after that.</p>
<p>The moves that I ended up making, the ones that actually pushed the
conversation forward, were all recommendations from friends and family.
The main points that worked was direct confrontation and setting
boundaries. I directly accused my chef of deliberately making the
situation worse for him and myself. Was it harsh? Yes, yes it was. Egos
were broken. But it was fair and it produced results.</p>
<p>In comparison, ChatGPT did not produce a single working strategy.
It's main point was to lay low and stay in the legal bounds. The logic
was when I don't make a ruckus, they can't punish me further. But this
forgoes reality! I already got undeserved punishment! If I would have
listened to the AI, I would still be chugging along in the company,
being miserable, and sitting on the punishments I received, for doing
nothing wrong except voicing my discontent with the situation.</p>

<img src="https://www.rismosch.com/articles/turned-upside-down/back_to_school.webp" alt="back to school" style="display: block; margin: auto; max-width: 100%;"/>

<p>But now I am out. The stress is over. My financial situation is not
optimal, but managable. But most importantly, psychologically, I am in a
much better shape than before.</p>
<p>I used the drama as motivation to apply to university again. I am
continuing with masters, and my current goal is a doctor title. I
attended <a href="https://s2025.siggraph.org/" target="_blank" rel="noopener noreferrer">SIGGRAPH 2025</a> last
year, and it showed me the possibilities in front of me. In my current
mind, a doctor would qualify me to work anywhere in the world. Of
course, on the job they've held my application and short notice against
me. But it's worth to mention that me applying to university was
<em>after</em> they punished me. So make of that what you will.</p>
<p>Anyway, I am still in the process of figuring out how this whole
study thing works. It isn't as easy as I thought. Homework is doable,
but time intensive. But while I am still stressed, it is comparably
bearable. For the first time in 6 years, I have an active social life. I
got to explore myself deeper, finding my identity, which previously I
had to suppress. And the goal I have set myself is clearly defined, and
achievable.</p>
<p>But also, the engine is progressing, very slowly, but surely. Even
less this year, as much of my time goes into studying. And I've been
putting some work into Blender, which is adjecently related to my
engine. But it's progress nontheless, and thus I am counting it. I am
especially proud about my fur solution, which I think will look amazing
once I get to render it. Below is a rough sketch of the idea.</p>

