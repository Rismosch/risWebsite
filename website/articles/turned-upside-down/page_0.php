<p>Also worth mentioning, the Wii sidequest wasn't for nothing. It gave
me insights into even better solutions. I am already overhauling the
asset system. And in the future I also want to redo the ECS system. They
could be faster and more suited to what I actually need. I realized that
I need to specialize more. Up until now I have programmed into the void,
features with no clear goal. Now my design requirements are starting to
tighten, and some of my current solutions are too generic to be
useful.</p>
<p>But whatever the future brings, today I feel stronger than yesterday.
I feel like I am more myself than previously, and a heavy weight has
been dropped off of my chest.</p>
