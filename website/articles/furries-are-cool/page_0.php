<p>In the last post I've briefly mentioned that I explored my identity
more deeply, due to previously suppressing it for the job. I guess this
is my coming out as a furry. I don't know, I just think they are
cute<a href="https://www.rismosch.com/articles/furries-are-cool/i_just_think_theyre_neat.webp" target="_blank" rel="noopener noreferrer">.</a>
In fact, I even went through a whole identity crisis about it, which
was a lot of fun. So let's talk about it.</p>

<img src="https://www.rismosch.com/articles/furries-are-cool/cute.webp" alt="Image: Cuteness overload!!1!" style="display: block; margin: auto; max-width: 100%;"/>

<p>I guess my story started similar to most others. In my childhood, I
was quite fond of animation and animals in media, and still am to this
day. For a long time I was ashamed about it, fearing to share my
interests with others. But to my dismay, it developped into a full blown
fetish. How do the chronically online kids call this? I goon to
boykisser<a href="https://www.rismosch.com/articles/furries-are-cool/Silly_Cat_(Character).webp" target="_blank" rel="noopener noreferrer">.</a></p>
<p>Part of me opening up is also becomming more comfortable talking
about sexuality. Thus I try not to be ashamed of admitting my fetishes.
Suppression was just part of the daily job. I mean, my real job.
Therefore, the situation was fine for a long long time. But once I got
fired, things went kind of messy. Suppression did not seem to work
anymore. And all kinds of feelings started to bubble up.</p>
<p>As studying proved to be more difficult than initially suspected, a
new fear kicked in: I may fail an exam. I never put in much effort in
school, neither in my bachelors, which my average grades reflect. But
now that I care, and try, the thought of failing developped into an
actual insecurity.</p>
<p>Multiple problems are colliding here. First, since I actively chose
to study, failing would suggest that I chose incorrectly, and that I am
not worthy to be a graphics programmer. Secondly, I don't have a
financial income at the moment. While I do have savings, I don't have
enough money for my entire studies. I am working on a solution, but
failing an exam would mean that I need to attend additional semesters.
This would make my studies take longer, stretching my already thin
finances even thinner.</p>

<img src="https://www.rismosch.com/articles/furries-are-cool/soup.webp" alt="Image: I've got the things for the soup" style="display: block; margin: auto; max-width: 100%;"/>

<p>Additionally, my social life increased a lot. I met many new people,
and I suspect my social circle will fluctuate over the coming years.
What hit me like a bombshell though, was talking to more trans people. I
consider myself an ally. This definitely isn't the first time I talked
to trans people. I think I know the lingo and I try to be accepting.</p>
<p>But what struck me is the confidence in transitioning. Fascism is on
the rise globally, and trans people are actively bullied and
discriminated by the larger society<a href="https://www.rismosch.com/articles/furries-are-cool/society.webp" target="_blank" rel="noopener noreferrer">.</a> Choosing
to transition anyway requires much bravery and a lot of force of will. I
can only dream about this level of self confidence, and thus I must
respect decisions like this.</p>
<p>Multiple times a week I meet many different people and we do homework
together. And I do notice the steps they are taking to transition… but
they don't directly tell me about it. I guess that is fair, since I also
don't talk about my genitals. But for some illogical reason this still
feels like betrayal. And now I tell myself that they think of me as an
untrustworthy person. This stands in stark contrast to my deep respect I have
of them.</p>
<p>Also I don't mention or talk about it, for the fear of making things
about myself. In my mind this is my insecurity. Talking about it would
improve things for me, maybe, but it would definitely make things
awkward for them.</p>

