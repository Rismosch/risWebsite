<p>To recap. I began thinking that I chose incorrectly going to university, the
furry feelings bubble up again, I met people, who compared to me don't
have a stick up their arse, and I feel like they don't trust me. So,
push comes to shove, cognitive dissonance, yadda yadda, and I was
reaching a fucking breaking point.</p>
<p>One day, a friend randomly flashed me with "I am no furry, but <a href="https://www.rismosch.com/articles/furries-are-cool/nick_wilde.webp" target="_blank" rel="noopener noreferrer">Nick
Wilde</a> is my hear me out." This sentence shook me to my core. Something
like this would've never come over my lips. You see, I was so afraid
about my feelings, that I avoided <em>any</em> association with furries.
I would've never said I'd be into furries. Nor would I have claimed that
I hated them. So, someone directly telling me about furries really caught me
off guard.</p>
<p>So, I told her about my feelings. I opened up, and to my relief, she
did not mind. Like at all. It's fine. There is nothing
to worry about it. People are weird and so am I. Most surprising of all,
this reveal does not change any social relationships I am currently
building. If anything, it makes things easier, because now I am not
pretending anymore. And who knows, maybe I'll meet other furries?</p>

<img src="https://www.rismosch.com/articles/furries-are-cool/sleeping.webp" alt="Image: sleepy time…" style="display: block; margin: auto; max-width: 100%;"/>

<p>I am not going out of my way and tell everyone about the furry thing.
But if it comes up again, for one reason or another, I now feel
comfortable talking about it. Opening up gave me a huge confidence
boost, which other friends have picked up on already.</p>
<p>The main reason I post this publicly, is to hold myself accountable
and to underline my newfound confidence. I still don't get the whole
fursona thing. I mean, isn't this still pretending? But in any case, I
am starting to believe that I am heading in the right direction: Getting
deeper into graphics programming, progressing my engine and game,
continuing with my studies. Actually meeting people again and having a
more active social life.</p>
<p>My thoughts have cleared again, and I can say that I feel fine. Happy
even. And things are okay the way they currently are.</p>
